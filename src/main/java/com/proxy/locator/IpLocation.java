package com.proxy.locator;

public class IpLocation {
    private String country;
    private String city;
    private String isp;
    private String lat;
    private String lon;

    public String getCountry() {
        return country;
    }

    public String getCity() {
        return city;
    }

    public String getIsp() {
        return isp;
    }

    public String getLat() {
        return lat;
    }

    public String getLon() {
        return lon;
    }
}
