package com.proxy.checker;

public class ProxyResultModel {
    boolean result;
    String comparisionResult;
    String location;


    public boolean getResult() {
        return result;
    }

    public void setResult(boolean result) {
        this.result = result;
    }

    public String getComparisionResult() {
        return comparisionResult;
    }

    public void setComparisionResult(String comparisionResult) {
        this.comparisionResult = comparisionResult;
    }

    public boolean isResult() {
        return result;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }
}
